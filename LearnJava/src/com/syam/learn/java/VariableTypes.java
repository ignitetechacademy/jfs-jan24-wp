package com.syam.learn.java;

public class VariableTypes {

	public static void main(String[] args) {
		
		// byte can hold 256 values
		byte age = (byte) 112l;  // -127 to -1     0     1 - 127 
		System.out.println("Age of Guinnes record holder : "+age);

		//  4 categories of data type
		// whole numbers - integral numbers
		byte departments; // department count
		short balance = (short) 843222;
		int networth = 250000000;
		long gdp = 5000000000000l; 
		
		float taxPercentage = 7.25f;
		double totalSales = 2587435.63;
		
		boolean exists = true;
		char gender = 'f';
	}

}
